def reformat_languages(languages)
  new_hash = {}
  languages.each do |style, info|
    info.each do |language, lang_attributes|
      lang_attributes.each do |type, type_value|
        new_hash = {language => {type => "#{type_value}", :style => [style]}}
      
        end
      end
     new_hash
  end
end